##Arithmetic_operators
print('The value of 3+4 is:', 3+4)
print('The value of 3-4 is:', 3-4)
print('The value of 3*4 is:', 3*4)
print('The value of 3/4 is:', 3/4)
print()



##Assignment_operators
value = 20

print('Real value:', value)

value += 2
print('Value += 2:', value)

value -= 2
print('Value -= 2:', value)

value *= 2
print('Value *= 2:', value)

value /= 2
print('Value /= 2:', value)
print()



##Comparision_operators
result = (3>4)
print('(3>4):', result)

result = (3<4)
print('(3<4)', result)

result = (4<=3)
print('(4<=3):', result)

result = (4==4)
print('(4==4):', result)

result = (3!=4)
print('(3!=4)', result)
print()



##Logical operators
bool1 = True
bool2 = False

print('bool1:', bool1)
print('bool2:', bool2)

print('The value of bool1 and bool2 is:', (bool1 and bool2))

print('The value of bool1 or bool2 is :', (bool1 or bool2))

print('The value of not bool2 is:', (not bool2))