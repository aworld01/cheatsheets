a = 5 #assign 5 to a
print(f"a => {a}")

a += 1 #add and assign
print(f"a += 1: {a}")

a -= 1 #substract and assign
print(f"a -= 1: {a}")

a *= 2 #multiply and assing
print(f"a *= 2: {a}")

a /= 2 #divide and assign
print(f"a /= 2: {a}")

a %= 2 #remainder and assign
print(f"a %= 2: {a}")

c = 5
c //= 2 #floor division and assign
print(f"c //= 2: {c}")

d = 6
d **= 2 #power and assign
print(f"d **= 2: {d}")