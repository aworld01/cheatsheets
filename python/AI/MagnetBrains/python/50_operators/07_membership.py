a = "Abdul Zoha"
b = ("apple", "mango", "banana", "grapes")
c = ["apple", "mango", "banana", "grapes"]
d = {"apple", "mango", "banana", "grapes", "mango"}
e = {1:"apple", 2:"mango", 3:"banana", 4:"grapes"}

print(a)
print(b)
print(c)
print(d)
print(e)
print()

"""in"""
print("Abdul" in a)
print("mango" in b)
print("mango" in c)
print("mango" in d)
print(2 in e)
print()

"""not in"""
print("Abdul" not in a)
print("mango" not in b)
print("mango" not in c)
print("mango" not in d)
print(2 not in e)

